    <p>
      <b>"Type III: Iterations"</b> records the process stages of this painting over seven years.
    </p>

    <p>
      The temporal units of the painting process
      <br />
      (canvas on oil) have been documented digitally and manifested as virtual artifacts, taking the
      form of videos and 3d sculptures.
    </p>

    <p>
      The work is both success and failure, the motive halfway discarded.
      <br />
      It is a living act consisting of frustration and pleasure.
    </p>

    <p>
      By documenting and storing the intermediate steps of the painting, the time component
      becomes a crucial part of this work series. Lined up snapshots become narrative, actors
      emerge, become carriers of the whole story, just to leave their position again and again
      until they fade away.
    </p>

