  <h3>
    WHAT if the medium fails to disappear by using a non-normative way of generating perception,
    by taking advantage of the error/glitch derived by the medium itself?
  </h3>

  <div class="content">
    <p>
      <span class="bold uppercase">Real Mirror</span>
      is an interactive object that transforms the viewer's self-image into new, spatial and physical
      forms of perception. The constructed virtual space is based on a VR system that is accessible
      through smart glasses. The interface between body and virtual space - a navigation sphere - provides
      a virtual 360° view of the environs and reacts on spectator’s movements of the sphere. In these
      rooms the user can navigate and lose him/herself at the same time. An interplay between two influencing
      dynamics: the changing environment, triggered by one's own movement in space.
    </p>

